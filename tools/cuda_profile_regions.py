def plot_profile(data, manifest, clock_khz, kernel_name,
                 output_path):
    """Generate profile visualization plots."""
    try:
        import matplotlib
        matplotlib.use('Agg')
        import matplotlib.pyplot as plt
    except ImportError:
        print("matplotlib not available, skipping plots.")
        return

    active = data[data.any(axis=1)]
    if len(active) == 0:
        return

    avg = active.mean(axis=0)

    # Pick top-level regions for pie chart
    top_regions = [
        r for r in manifest["regions"]
        if r["depth"] == 1
    ]
    if not top_regions:
        top_regions = [
            r for r in manifest["regions"]
            if r["parent_id"] is None
        ]

    labels = []
    sizes = []
    for r in top_regions[:8]:
        short = r["label"].split(":")[-1]
        labels.append(short)
        sizes.append(max(0, float(avg[r["id"]])))

    nonzero = [
        (l, s) for l, s in zip(labels, sizes) if s > 0
    ]
    if not nonzero:
        return
    labels, sizes = zip(*nonzero)

    fig, axes = plt.subplots(1, 2, figsize=(14, 6))

    axes[0].pie(sizes, labels=labels, autopct='%1.1f%%')
    axes[0].set_title(
        f"{kernel_name.upper()} Top-Level Breakdown"
    )

    totals = active[:, 0]
    times_ms = totals / (clock_khz * 1.0)
    axes[1].hist(times_ms, bins=min(50, len(active)),
                 edgecolor='black', alpha=0.7)
    axes[1].set_xlabel("Total time (ms)")
    axes[1].set_ylabel("Count")
    axes[1].set_title(
        f"{kernel_name.upper()} Total Time Distribution"
    )

    plt.tight_layout()
    plt.savefig(output_path, dpi=150)
    print(f"\nPlot saved to {output_path}")


