#!/usr/bin/env python3
"""CUDA kernel region profiling via clock64() instrumentation.

Runs SA or Gibbs kernel with auto-generated profiling enabled,
reads back per-region cycle counters, and outputs a hierarchical
breakdown showing where GPU time is spent inside the kernel.

Region names, source mappings, and hierarchy are loaded from
a JSON manifest generated by cuda_auto_profiler.py — nothing
is hardcoded.

Usage:
    python tools/cuda_profile_regions.py [--kernel gibbs|sa] \
        [--sweeps N] [--reads N] [--plot] [--flame] \
        [--flame-html]
"""

import argparse
